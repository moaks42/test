#include <unistd.h>

void	ft_putstr(char *str)
{
	int	pepa;

	pepa = 0;
	while (str[pepa] != '\0')
	{
		write(1, &str[pepa], 1);
		pepa++;
	}
}
int main(void)
{
 ft_putstr("lol");
}
